package animal;

public class Animal {
	String nome;
	double comprimento;
	int numPatas;
	String cor;
	String ambiente;
	double vmed;
	
	Animal(String nome, double comprimento, int numPatas, String cor, String ambiente, double vmed) {
		this.nome = nome;
		this.comprimento = comprimento;
		this.numPatas = numPatas;
		this.cor = cor;
		this.ambiente = ambiente;
		this.vmed = vmed;
	}
}
